# Conditional statements are used to execute a statement or a group of statements, when some condition is true.
# Type - if, Elif, Else

age = 34
if age >= 18:
    print("Inside if block")
    print(" GO ahead and vote")
elif age >30:
    print("Adult")
elif age > 40:
    print("Mature")
else:
    print("Enjoy")

